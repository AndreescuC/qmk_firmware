bool ENABLE_SINGLE_STEP  = true;  // Ctrl + I/K/J/L → arrows
bool ENABLE_FIVE_LINES   = true;  // Ctrl + Opt + I/K → 5× Up/Down
bool ENABLE_PAGE_NAV     = true;  // Ctrl + Opt + Cmd + I/K → PgUp/PgDn

static void send_arrow(uint16_t kc) {
    tap_code(kc);
}

static void send_five_lines(bool is_up) {
    if (!ENABLE_FIVE_LINES) return;
    uint16_t kc = is_up ? KC_UP : KC_DOWN;
    for (uint8_t i = 0; i < 5; i++) {
        send_arrow(kc);
    }
}

static void send_page_nav(bool is_up) {
    if (!ENABLE_PAGE_NAV) return;
    tap_code(is_up ? KC_PGUP : KC_PGDN);
}


bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (!record->event.pressed) return true;
    if (!(layer_state & (1UL << _NAV))) return true;
    if (!(get_mods() & MOD_MASK_CTRL)) return true;

    uint8_t saved_mods = get_mods();
    uint8_t ctrl_only = saved_mods & MOD_MASK_CTRL;
    del_mods(ctrl_only);

    switch (keycode) {
        case KC_I:
        case KC_K:
            if (ENABLE_SINGLE_STEP) {
                if (saved_mods & MOD_MASK_ALT) {
                    if (saved_mods & MOD_MASK_GUI && ENABLE_PAGE_NAV) {
                        send_page_nav(keycode == KC_I);
                    } else {
                        send_five_lines(keycode == KC_I);
                    }
                } else {
                    send_arrow(keycode == KC_I ? KC_UP : KC_DOWN);
                }
            }
            break;
        case KC_J:
            if (ENABLE_SINGLE_STEP) send_arrow(KC_LEFT);
            break;
        case KC_L:
            if (ENABLE_SINGLE_STEP) send_arrow(KC_RIGHT);
            break;
        default:
            return true;
    }

    set_mods(saved_mods);
    return false;
}
